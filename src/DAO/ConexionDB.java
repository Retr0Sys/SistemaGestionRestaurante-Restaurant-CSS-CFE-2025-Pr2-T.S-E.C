package DAO;

import java.sql.Connection;
import java.sql.DriverManager;
import java.sql.SQLException;

public class ConexionDB
{
    private static final String URL  = "jdbc:mysql://localhost:3306/pruebaprogramacion";
    private static final String USER = "root";
    private static final String PASS = "1234";

    // Establece y devuelve la conexión con la base de datos
    public static Connection getConnection() throws SQLException
    {
        return DriverManager.getConnection(URL, USER, PASS); // Crea la conexión usando los datos definidos
    }
}
